public class GtreeIllegalSourceException extends Exception {
  public GtreeIllegalSourceException() { }
  public GtreeIllegalSourceException(String message) {
    super(message);
  }
  public GtreeIllegalSourceException(Throwable cause) {
    super(cause);
  }
  public GtreeIllegalSourceException(String message, Throwable cause) {
    super(message, cause);
  }
}

